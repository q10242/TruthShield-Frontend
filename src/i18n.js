const browserLocale = typeof navigator === 'undefined' ? DEFAULT_LOCALE : navigator.language
const initialLocale = localStorage.getItem(STORAGE_KEY)
  || (browserLocale.toLowerCase().startsWith('zh') ? 'zh-TW' : 'en')

const state = reactive({
  locale: messages[initialLocale] ? initialLocale : DEFAULT_LOCALE,
})

function lookup(path, locale = state.locale) {
  return path.split('.').reduce((current, part) => current?.[part], messages[locale])
}

export function setLocale(locale) {
  if (!messages[locale]) return
  state.locale = locale
  localStorage.setItem(STORAGE_KEY, locale)
}

export function useI18n() {
  const locale = computed(() => state.locale)

  function t(path, params = {}) {
    let value = lookup(path) ?? lookup(path, DEFAULT_LOCALE) ?? path
    Object.entries(params).forEach(([key, replacement]) => {
      value = String(value).replaceAll(`{${key}}`, String(replacement))
    })
    return value
  }

  return {
    locale,
    locales,
    setLocale,
    t,
  }
}

